package examen.modelo;

public class Usuario {

	private String nombre;
	private String edad;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getEdad() {
		return edad;
	}
	public void setEdad(String string) {
		this.edad = string;
	}
	
	
}
